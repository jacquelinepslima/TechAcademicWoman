package exercicio.uri.amanda_medeiros.util;

public class EstruturaRepeticaoPara {
    public static void main(String[] args) {
        for (int i=0; i<5; i++){
            System.out.println("Executando o FOR incremento ++ => " + i);
        }
        System.out.println();
        System.out.println();
        for (int i=5; i>0; i--){
            System.out.println("Executando o FOR decremento -- => " + i);
        }
    }
}
