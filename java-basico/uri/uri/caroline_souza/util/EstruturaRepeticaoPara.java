package exercicio.uri.caroline_souza.util;

public class EstruturaRepeticaoPara {
	//para uma estrutura de reoeticao FOR(para) eu sei quantas vezes
	// eu vou repetir esse codigo do laço 
	public static void main(String[]args) {
		//criar variavel condicao incremento ou decremento
		for(int i =1; i <= 5; i++) {
			System.out.println("Executando com o FOR INCREMENTO++" +i);
			
		}
		for(int i = 5; i >0 ; i--) {
			System.out.println("Incremento"+i);
		}
	}

}
