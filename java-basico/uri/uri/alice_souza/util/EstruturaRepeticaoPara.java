package exercicio.uri.alice_souza.util;

public class EstruturaRepeticaoPara {
    public static void main(String[] args) {
        for(int i = 1; i<=5; i++){
            System.out.println("executando for com incremento. i= " + i);
        }
        System.out.println();
        for(int i = 5; i>0; i--){
            System.out.println("executando for com decremento. i= " + i);
        }
    }
}

