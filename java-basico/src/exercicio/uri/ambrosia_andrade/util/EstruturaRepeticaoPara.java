package exercicio.uri.ambrosia_andrade.util;

public class EstruturaRepeticaoPara {
    public static void main(String[] args) {
        // Incremento
        for(int i = 1; i <= 5; i++){
            System.out.println("i: " + i);
        }
        System.out.println();

        // Decremento
        for(int i = 5; i > 0; i--){
            System.out.println("i: " + i);
        }
        System.out.println();
    }
}
