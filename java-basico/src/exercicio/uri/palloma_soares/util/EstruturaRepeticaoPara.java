package exercicio.uri.palloma_soares.util;

public class EstruturaRepeticaoPara {

    //Para uma estrutura de repeticao FOR(para) eu sei quantas vezes eu vou repetir esse codigo do lo√ßo
    public static void main(String[] args) {
        // criar variavel;  condicao;  incremento ou decremento
        for(int i=1;        i<=5;        i++){
        System.out.println("Executando com o FOR INCREMENTO ++ =>"+i);
        }
        System.out.println();
        System.out.println();

        for(int i=5;        i>0;        i--){
            System.out.println("Executando com o FOR DECREMENTO -- =>"+i);
        }
    }
}
