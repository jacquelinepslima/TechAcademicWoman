package exercicio.uri.samyra_figueiredo.util;

public class EstruturaRepeticaoPara {

    //Para uma estrutura de repeticao FOR (para) EU SEI QUANTAS VEZES EU VOU REPETIR ESSE CODIGO DO LAÃ‡O

    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.println("Executando com o FOR INCREMENTO ++ --> " + i);
        }

        System.out.println();
        System.out.println();

        for (int i = 5; i > 0; i--) {
            System.out.println("Executando com o FOR DECREMENTO -- --> " + i);
        }
    }
}
