package exercicio.uri.amanda_alves.util;

public class EstruturaRepeticaoPara {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++){
            System.out.println("Executa incremento: " +i);
        }
        
        for (int i = 5; i > 0; i--){
            System.out.println("Executa decremento: " +i);
        }
    }

}
