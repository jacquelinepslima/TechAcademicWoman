package exercicio.uri.bruna_lima.util;

public class EstruturaRepeticaoPara {
    public static void main(String[] args) {

        // Estrutura de repetição FOR - Tem limite de vezes que irá repetir
        // Cria variável, inicializa, limita quantas vezes irá repetir e incrementa no contador i

        for(int i = 0; i < 5; i++) {
            System.out.println("Executando com o FOR");
        }
    }
}
