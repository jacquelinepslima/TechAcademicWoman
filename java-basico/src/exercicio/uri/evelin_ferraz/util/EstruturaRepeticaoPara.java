package exercicio.uri.evelin_ferraz.util;

public class EstruturaRepeticaoPara {

    //Para uma estrutura de repeticao FOR (para) EU SEI QUANTAS VEZES EU VOU REPETIR ESSE CODIGO DO LACO;
    public static void main(String[] args) {
        //criar variavel; condicao incremento ou decremento
        for(int i = 0; i <=5; i++){
            System.out.println("Executando com o FOR INCREMENTO ++ =>" + i);
        }
        System.out.println();
        System.out.println();

        for(int i = 5; i > 0; i--){
            System.out.println("Executando com o FOR INCREMENTO -- =>" + i);
        }
    }
}
