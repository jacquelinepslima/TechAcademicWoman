package exercicio.uri.giovanna_santana.util;

public class EstruturaDeRepeticaoPara {

    //Para uma estrutura de repeticao FQR (para) EU SEI QUANTAS VEZES EU VOU REPETIR ESSE CODIGO DO LACO
    public static void main(String[] args) {
        //crair variavel condicao   incremento ou decremento
        for (int i = 1;     i<= 5;      i++){
            System.out.println("Executando com o FOR INCREMENTO ++ =>" + i);
        }

        System.out.println();
        System.out.println();
        for (int i = 5;     i > 0;      i--){
            System.out.println("Executando com o FOR DECREMENTO -- =>" + i);
        }


    }
}
