package exercicio.uri.valquiria_matter.util;

public class EstruturaRepeticaoPara {

    public static void main(String[] args) {

        for(int i=1; i<=5; i++){
            System.out.println("Executando o FOR com incremento: " + i);
        }

        System.out.println();
        System.out.println();

        for(int i=5; i>0; i--){
            System.out.println("Executando o FOR com decremento: " + i);
        }

    }

}
